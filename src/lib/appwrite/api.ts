// ============================== SIGN IN
export async function signInAccount(user: { email: string; password: string }) {
  try {
    // Check if there's already an active session
    let currentSession;
    try {
      currentSession = await account.get();
    } catch (error) {
      // If there's no active session, account.get() will throw an error, which we can ignore
      currentSession = null;
    }

    // If a session is already active, return it
    if (currentSession) {
      console.log("A session is already active.");
      return currentSession;
    }

    // If no session is active, create a new session
    const session = await account.createEmailPasswordSession(
      user.email,
      user.password
    );
    return session;
  } catch (error) {
    if (error instanceof AppwriteException) {
      if (error.code === 401) {
        console.log("Unauthorized: Please check your credentials.");
      } else {
        console.log(`Appwrite error: ${error.message}`);
      }
    } else {
      console.log(error);
    }
  }
}

